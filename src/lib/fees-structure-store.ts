// Fees Structure & Automated Billing Engine - in-memory store
// Integrates with GL revenue accounts, AR, invoicing, SIS and academic calendar.

export type BillingCycle = 'Monthly' | 'Termly' | 'Annual' | 'One-time' | 'Custom';
export type StructureStatus = 'Draft' | 'Pending Approval' | 'Approved' | 'Archived';
export type InvoiceStatus = 'Draft' | 'Posted' | 'Paid' | 'Partially Paid' | 'Cancelled';

export interface FeeItem {
  id: string;
  name: string;
  glAccountCode: string; // links to GL revenue account
  amount: number;
  cycle: BillingCycle;
  mandatory: boolean;
  appliesTo?: 'All' | 'Boarding' | 'Day';
}

export interface FeeStructure {
  id: string;
  code: string;
  name: string;
  academicYear: string;
  level?: string;         // e.g. "Form 3"
  className?: string;     // e.g. "Form 3A"
  stream?: string;
  program?: string;
  currency: string;
  version: number;
  parentId?: string;      // previous version
  status: StructureStatus;
  effectiveFrom: string;
  effectiveTo?: string;
  items: FeeItem[];
  createdBy: string;
  createdAt: string;
  approvedBy?: string;
  approvedAt?: string;
  notes?: string;
}

export interface AcademicTerm {
  id: string;
  name: string;           // "Term 1 2026"
  academicYear: string;
  startDate: string;
  endDate: string;
  billingDate: string;    // day invoices auto-generate
}

export interface StudentAssignment {
  id: string;
  studentId: string;
  structureId: string;
  discountPercent?: number;
  scholarshipAmount?: number;
  notes?: string;
  assignedAt: string;
}

export interface GeneratedInvoice {
  id: string;
  invoiceNumber: string;
  studentId: string;
  structureId: string;
  termId?: string;
  date: string;
  dueDate: string;
  lines: { description: string; glAccountCode: string; amount: number }[];
  subtotal: number;
  discount: number;
  total: number;
  status: InvoiceStatus;
  currency: string;
  autoGenerated: boolean;
}

export interface AuditEntry {
  id: string;
  timestamp: string;
  actor: string;
  action: string;
  entity: string;
  entityId: string;
  details: string;
}

