// ---------------- Helpers ----------------
export function structureTotal(structure: FeeStructure, boardingStatus?: 'Boarding' | 'Day'): number {
  return structure.items
    .filter(i => !i.appliesTo || i.appliesTo === 'All' || i.appliesTo === boardingStatus)
    .filter(i => i.mandatory)
    .reduce((s, i) => s + i.amount, 0);
}

export function structureFullTotal(structure: FeeStructure, boardingStatus?: 'Boarding' | 'Day'): number {
  return structure.items
    .filter(i => !i.appliesTo || i.appliesTo === 'All' || i.appliesTo === boardingStatus)
    .reduce((s, i) => s + i.amount, 0);
}

export function generateInvoiceForAssignment(
  assignment: StudentAssignment,
  structure: FeeStructure,
  term: AcademicTerm,
  boardingStatus: 'Boarding' | 'Day',
  seq: number,
): GeneratedInvoice {
  const applicable = structure.items.filter(i => {
    if (i.cycle === 'One-time' && term.id !== 't1') return false;
    if (i.appliesTo && i.appliesTo !== 'All' && i.appliesTo !== boardingStatus) return false;
    return true;
  });
  const lines = applicable.map(i => ({ description: i.name, glAccountCode: i.glAccountCode, amount: i.amount }));
  const subtotal = lines.reduce((s, l) => s + l.amount, 0);
  const discountPct = assignment.discountPercent || 0;
  const scholarship = assignment.scholarshipAmount || 0;
  const discount = subtotal * (discountPct / 100) + scholarship;
  const total = Math.max(0, subtotal - discount);
  const dueDate = new Date(term.startDate);
  dueDate.setDate(dueDate.getDate() + 14);
  return {
    id: `gi-${Date.now()}-${seq}`,
    invoiceNumber: `AUTO-${term.id.toUpperCase()}-${String(seq).padStart(4, '0')}`,
    studentId: assignment.studentId,
    structureId: structure.id,
    termId: term.id,
    date: term.billingDate,
    dueDate: dueDate.toISOString().split('T')[0],
    lines, subtotal, discount, total,
    status: 'Posted',
    currency: structure.currency,
    autoGenerated: true,
  };
}

export function agingBuckets(invoices: GeneratedInvoice[], asOf: Date = new Date()) {
  const buckets = { current: 0, b30: 0, b60: 0, b90: 0, over90: 0 };
  invoices
    .filter(i => i.status !== 'Paid' && i.status !== 'Cancelled')
    .forEach(i => {
      const due = new Date(i.dueDate);
      const days = Math.floor((asOf.getTime() - due.getTime()) / (1000 * 60 * 60 * 24));
      if (days <= 0) buckets.current += i.total;
      else if (days <= 30) buckets.b30 += i.total;
      else if (days <= 60) buckets.b60 += i.total;
      else if (days <= 90) buckets.b90 += i.total;
      else buckets.over90 += i.total;
    });
  return buckets;
}
